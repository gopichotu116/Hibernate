    private void jButton15ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton15ActionPerformed
        // TODO add your handling code here:
        switch(pilih){
        case 1:
            bil2 = Double.parseDouble(String.valueOf(jTextArea1.getText()));
            jumlah = bil1+bil2; 
            bilangan = nf.format(jumlah);
            break;
        case 2:
            bil2 =Double.parseDouble(String.valueOf(jTextArea1.getText()));
            jumlah = bil1 - bil2;
            bilangan = nf.format(jumlah);          
            break;
        case 3:
            bil2 = Double.parseDouble(String.valueOf(jTextArea1.getText()));
            jumlah = bil1 * bil2;
            bilangan = nf.format(jumlah);          
            break;
        case 4:
            bil2 = Double.parseDouble(String.valueOf(jTextArea1.getText()));
            jumlah = bil1 / bil2;
            bilangan = nf.format(jumlah);           
            break;            
        } 
        
        jTextArea1.setText(bilangan);
    }//GEN-LAST:event_jButton15ActionPerformed

